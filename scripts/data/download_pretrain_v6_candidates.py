def download_germanquad(args: argparse.Namespace, out_dir: Path) -> dict[str, Any]:
    writer = Writer(out_dir, "germanquad_train")
    stats: Counter[str] = Counter()
    rows: Iterable[dict[str, Any]]
    try:
        rows = load_dataset("deepset/germanquad", split="train", **hf_kwargs(streaming=True))
    except RuntimeError as exc:
        if "Dataset scripts are no longer supported" not in str(exc):
            raise
        cache_dir = out_dir / "_downloads" / "germanquad"
        cache_dir.mkdir(parents=True, exist_ok=True)
        zip_path = cache_dir / "GermanQuAD.zip"
        if not zip_path.is_file():
            urllib.request.urlretrieve("https://germanquad.s3.amazonaws.com/GermanQuAD.zip", zip_path)
        extract_dir = cache_dir / "extracted"
        train_path = extract_dir / "GermanQuAD" / "GermanQuAD_train.json"
        if not train_path.is_file():
            with zipfile.ZipFile(zip_path) as zf:
                zf.extractall(extract_dir)

        def iter_germanquad_json() -> Iterable[dict[str, Any]]:
            payload = json.loads(train_path.read_text(encoding="utf-8"))
            for article in payload.get("data", []):
                title = article.get("title")
                for paragraph in article.get("paragraphs", []):
                    context = paragraph.get("context")
                    document_id = paragraph.get("document_id")
                    for qa in paragraph.get("qas", []):
                        yield {
                            "id": qa.get("id"),
                            "title": title,
                            "document_id": document_id,
                            "context": context,
                            "question": qa.get("question"),
                            "answers": {"text": [a.get("text") for a in qa.get("answers", []) if a.get("text")]},
                        }

        rows = iter_germanquad_json()
    for row in rows:
        stats["seen"] += 1
        question = clean(row.get("question"))
        context = clean(row.get("context"))
        answers = row.get("answers") or {}
        answer_texts = answers.get("text") if isinstance(answers, dict) else None
        answer = clean(answer_texts[0] if answer_texts else "")
        if not question or not answer or not context:
            stats["missing_field"] += 1
            continue
        text = f"Kontext: {context}\nFrage: {question}\nAntwort: {answer}"
        reason = reject_text(text, min_chars=120, max_chars=8000, min_words=12, max_urls=1)
        if reason:
            stats[f"reject_{reason}"] += 1
            writer.reject({"reason": reason, "question": question[:400], "context": context[:400]})
            continue
        writer.write(
            {
                "source": "deepset/germanquad",
                "license": "CC-BY-4.0",
                "id": row.get("id"),
                "title": row.get("title"),
                "question": question,
                "answer": answer,
                "context": context,
            },
            text,
        )
        stats["written"] += 1
        if stats["written"] >= args.max_germanquad:
            break
    return {"name": "germanquad_train", "stats": dict(stats), **writer.close()}


