def reject_codeparrot(row: dict[str, Any], min_chars: int, max_chars: int) -> list[str]:
    content = clean(row.get("content"))
    path = str(row.get("path") or "")
    license_name = str(row.get("license") or "unknown").lower()
    reasons: list[str] = []
    if license_name not in ALLOWED_CODE_LICENSES:
        reasons.append("license_not_allowed")
    if len(content) < min_chars:
        reasons.append("too_short")
    if len(content) > max_chars:
        reasons.append("too_long")
    if row.get("autogenerated") is True:
        reasons.append("autogenerated_field")
    if CODE_VENDOR_RE.search(path):
        reasons.append("vendor_path")
    if CODE_GENERATED_RE.search(content):
        reasons.append("generated")
    if CODE_SECRET_RE.search(content):
        reasons.append("possible_secret")
    try:
        ast.parse(content)
    except SyntaxError:
        reasons.append("python_syntax_error")
    return reasons


def download_codeparrot(args: argparse.Namespace, out_dir: Path) -> dict[str, Any]:
    ds = load_dataset("codeparrot/codeparrot-clean", split="train", **hf_kwargs(streaming=True))
    writer = Writer(out_dir, "codeparrot_clean_python_permissive")
    stats: Counter[str] = Counter()
    licenses: Counter[str] = Counter()
    for row in ds:
        stats["seen"] += 1
        licenses[str(row.get("license") or "unknown")] += 1
        reasons = reject_codeparrot(row, min_chars=80, max_chars=30000)
        if reasons:
            for reason in reasons:
                stats[f"reject_{reason}"] += 1
            writer.reject(
                {
                    "reasons": reasons,
                    "path": row.get("path"),
                    "repo_name": row.get("repo_name"),
                    "license": row.get("license"),
                    "preview": clean(row.get("content"))[:500],
                }
            )
            continue
        content = clean(row.get("content"))
        path = str(row.get("path") or "")
        repo = str(row.get("repo_name") or "")
        text = f"<|code|>[python]\n<filename>{path}\n<reponame>{repo}\n{content}\n<|endcode|>"
        writer.write(
            {
                "source": "codeparrot/codeparrot-clean",
                "license": row.get("license"),
                "repo_name": repo,
                "path": path,
                "content": content,
            },
            text,
        )
        stats["written"] += 1
        if stats["written"] >= args.max_codeparrot:
            break
        if stats["seen"] >= args.max_scan_codeparrot:
            break
    result = writer.close()
    result["licenses_seen_top20"] = dict(licenses.most_common(20))
    return {"name": "codeparrot_clean_python_permissive", "stats": dict(stats), **result}


DOWNLOADERS = {
    "alpaca_gpt4_de": download_alpaca_gpt4_deutsch,
    "oasst_de": download_oasst_de,
    "oasst_de_conversations": download_oasst_de_conversations,
    "germanquad": download_germanquad,
    "fineweb2_de": download_fineweb2_de,
    "openmathinstruct2": download_openmath,
    "codeparrot": download_codeparrot,
}


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output-dir", type=Path, default=Path("data/training/pretrain_v6_candidates"))
    parser.add_argument("--source", action="append", choices=sorted(DOWNLOADERS), default=None)
    parser.add_argument("--max-oasst", type=int, default=5000)
    parser.add_argument("--max-oasst-de-conversations", type=int, default=10000)
    parser.add_argument("--max-alpaca-gpt4-de", type=int, default=20000)
    parser.add_argument("--max-scan-alpaca-gpt4-de", type=int, default=60000)
    parser.add_argument("--max-germanquad", type=int, default=5000)
    parser.add_argument("--max-fineweb2", type=int, default=5000)
    parser.add_argument("--max-scan-fineweb2", type=int, default=60000)
    parser.add_argument("--max-openmath", type=int, default=5000)
    parser.add_argument("--max-scan-openmath", type=int, default=60000)
    parser.add_argument("--max-codeparrot", type=int, default=5000)
    parser.add_argument("--max-scan-codeparrot", type=int, default=80000)
    return parser.parse_args()


def main() -> None:
    args = parse_args()
    args.output_dir.mkdir(parents=True, exist_ok=True)
    selected = args.source or list(DOWNLOADERS)
    manifest: dict[str, Any] = {
        "created_at": now_iso(),
        "output_dir": str(args.output_dir),
        "sources": [],
        "caps": {
            "max_oasst": args.max_oasst,
            "max_oasst_de_conversations": args.max_oasst_de_conversations,
            "max_alpaca_gpt4_de": args.max_alpaca_gpt4_de,
            "max_germanquad": args.max_germanquad,
            "max_fineweb2": args.max_fineweb2,
            "max_openmath": args.max_openmath,
            "max_codeparrot": args.max_codeparrot,
        },
    }
    for name in selected:
        print(f"=== {name} ===", flush=True)
        try:
            result = DOWNLOADERS[name](args, args.output_dir)
            result["ok"] = True
        except Exception as exc:
            result = {"name": name, "ok": False, "error": repr(exc)}
            print(f"ERROR {name}: {exc!r}", flush=True)
        manifest["sources"].append(result)
        write_json(args.output_dir / "manifest.json", manifest)
    manifest["finished_at"] = now_iso()
    write_json(args.output_dir / "manifest.json", manifest)
    print(json.dumps(manifest, ensure_ascii=False, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
